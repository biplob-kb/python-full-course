# basic data types and variable
age = 45
interest_rate = 0.05
name = "John Doe"
is_active = True
district = None

print(district)  # Output: None
print(age)       # Output: 45
print(interest_rate)  # Output: 0.05
print(name)      # Output: John Doe
print(is_active) # Output: True

print(type(age))          # Output: <class 'int'>
print(type(interest_rate))  # Output: <class 'float'>
print(type(name))         # Output: <class 'str'>
print(type(is_active))    # Output: <class 'bool'>
print(type(district))     # Output: <class 'NoneType'>

